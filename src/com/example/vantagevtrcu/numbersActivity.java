	    void sendbutton(byte[] data) {
	    	
			SharedPreferences ip_2 = getSharedPreferences("prefs", MODE_PRIVATE);
			String ausgabe = ip_2.getString("ip", "0");
			int port = ip_2.getInt("port", 0);
			
			try {
				ib = InetAddress.getByName(ausgabe);
			} catch (UnknownHostException e1) {
				// TODO Auto-generated catch block
				e1.printStackTrace();
			}
	    	
	    	
			DatagramPacket packet = new DatagramPacket( data, data.length, ib, port );

			DatagramSocket toSocket = null;
			try {
				toSocket = new DatagramSocket();
				toSocket.send(packet);
			} catch (SocketException e) {
				// TODO Auto-generated catch block
				e.printStackTrace();
			} catch (IOException e) {
				// TODO Auto-generated catch block
				e.printStackTrace();
			}
			
			
		}
	

		

